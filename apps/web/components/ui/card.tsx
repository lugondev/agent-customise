import { HTMLAttributes, forwardRef } from 'react'
import { cn } from '@/lib/utils'

export interface CardProps extends HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'elevated' | 'outlined'
  padding?: 'none' | 'sm' | 'md' | 'lg'
}

const Card = forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = 'default', padding = 'md', children, ...props }, ref) => {
    // Minimalist card: outlined by default, no scaling, very light shadows
    const baseClasses = 'bg-background dark:bg-gray-900 rounded-lg transition-colors'
    
    const variantClasses = {
      default: 'border border-gray-200 dark:border-gray-700',
      elevated: 'border border-gray-200 dark:border-gray-700 shadow-sm',
      outlined: 'border border-gray-200 dark:border-gray-700 bg-transparent'
    }
    
    const paddingClasses = {
      none: '',
      sm: 'p-4',
      md: 'p-6',
      lg: 'p-8'
    }
    
    const classes = cn(
      baseClasses,
      variantClasses[variant],
      paddingClasses[padding],
      className
    )
    
    return (
      <div
        ref={ref}
        className={classes}
        {...props}
      >
        {children}
      </div>
    )
  }
)

Card.displayName = 'Card'

